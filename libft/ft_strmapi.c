#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		len;
	char	*res;
	int		i;

	len = ft_strlen(s);
	res = (char *)malloc(len + 1);
	i = 0;
	if (!res)
		return (NULL);
	if (!s || !f)
		return (NULL);
	while (i < len)
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[len] = '\0';
	return (res);
}
